import re

# Тест үшін мәтін
text = "Сәлем! Менің нөмірім +7(777)123-45-67. Бағасы 500.99$. 2024_test. ab abb abbb"

# 1. \d - Кез келген сан (0-9)
print("Сандар:", re.findall(r'\d', text))

# 2. \D - Сан емес кез келген таңба
print("Сан емес:", re.findall(r'\D', text))

# 3. \w - Әріп, сан немесе астын сызу (_)
print("Сөз таңбалары:", re.findall(r'\w', text))

# 4. \s - Бос орын (space, tab, newline)
print("Бос орындар саны:", len(re.findall(r'\s', text)))

# 5. . (нүкте) - Жаңа жолдан басқа кез келген 1 таңба
print("Нүктемен іздеу (а.б):", re.findall(r'a.b', text))

# 6. * - 0 немесе одан көп қайталану
print("Жұлдызша (ab*):", re.findall(r'ab*', text))

# 7. + - 1 немесе одан көп қайталану
print("Плюс (ab+):", re.findall(r'ab+', text))

# 8. ? - 0 немесе 1 рет (міндетті емес)
print("Сұрақ белгісі (ab?):", re.findall(r'ab?', text))

# 9. ^ - Мәтіннің басы
print("Басы 'Сәлем' ме?:", re.findall(r'^Сәлем', text))

# 10. $ - Мәтіннің соңы
# Ескерту: Экрандау арқылы нақты $ белгісін іздеу
print("Нақты доллар белгісі:", re.findall(r'\$', text))

# 11. [ ] - Таңбалар жиынтығы (диапазон)
print("А-Я аралығындағы әріптер:", re.findall(r'[А-Я]', text))

# 12. \ (Экрандау) - Арнайы таңбаны кәдімгі мәтін қылу
# Нақты нүктені және жақшаны іздеу
print("Нақты нүкте мен жақша:", re.findall(r'\.\d+\(', text))